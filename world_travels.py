# Program that sorts world cities
places = ['tokyo', 'melbourne', 'beijing', 'cairo', 'london']

print(places) # prints original list

print(sorted(places)) # prints list sorted 

print(places) # prints original list

print(sorted(places, reverse=True)) # temp sorts list in reverse

print(places) # prints orginal list 

places.reverse() # reverses order of list
print(places)

places.reverse() # Reverses list again
print(places)

places.sort() # permenenantly sorts list in alphabetical order
print(places)

places.sort(reverse=True)
print(places)





